# Operações matemáticas no python

num1 = 10
num2 = 5

# Adição
print(f'A soma é {num1+num2}')

#Subtração
print(f'A subtração é {num1-num2}')

#Multiplicação
print(f'A multiplicação é {num1*num2}')

#Divisão
print(f'A divisão é {num1/num2}')

x = 3
y = 2

#Divisão (parte inteira)
print(x//y)

#Exponenciação
print(f'O número elevado ao quadrado é {num1**2}')

#Resto da divisão
print(f'O resto da divisão de {num1} por {num2} é {num1 % num2}')


