#Exemplos de entrada de dados (input)

#Exemplo 1: Leitura de uma variável texto
nome_funcionario = input('Informe o nome do funcionário: ')
print(f'O conteúdo da variável é {nome_funcionario}')

#Exemplo 2: leiura de uma variável numérica inteira
idade_pessoa = int(input('informe a idade da pessoa: '))
print(f'A idade da pessoa é {idade_pessoa}')

#Exemplo 3: leitura de uma variável numérica real (casas decimais)
nota_aluno = float(input('Informe a nota do aluno: '))
print(f'A nota do aluno foi {nota_aluno}')