# Exemplos de saída de dados (exibição na tela)

#Exemplo 1: Exibir uma mensagem de texto na tela
print('Hello World')

#Exemplo 2: Definir uma variável variável numérica inteira e exibir seu conteúdo
numero_inteiro = 10
print(numero_inteiro)

#Exibir o tipo de dado da variável
print(type(numero_inteiro))

#Exemplo 3: Definir uma variável numérica real e exibir seu conteúdo
salario = 13500.75
print(type(salario))
print(salario)

#Exemplo 4: Definir uma variável numérica real e exibir seu conteúdo
nome_pessoa = 'Joao da Silva'
print(type(nome_pessoa))
print(nome_pessoa)

# Exemplo 5: Exibir uma mensagem de texto junto com o conteúdo da variável
print('O nome da pessoa é', nome_pessoa)

#Outra forma de exibir texto com variável
print(f'O salário do funcionário é R${salario}')
